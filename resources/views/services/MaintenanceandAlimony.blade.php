<h1 class="text">Maintenance & Alimony</h1>


<div class="center">
    <p>When a married couple gets a divorce, the court may award “alimony” or spousal support to one of the former spouses, based either on an agreement between the couple or a decision by the court itself. The purpose of alimony is to limit any unfair economic effects of a divorce by providing a continuing income to a non-wage-earning or lower-wage-earning spouse. Maintenance is granted to wife’s who are unable to maintain themselves. Maintenance can also be classified in to two parts:</p>

    <p>Interim Maintenance:</p>

    <p>During the pendency of the proceeding regarding monthly allowance for the maintenance, the court orders such person to make a monthly allowance for the interim maintenance of his wife or such child, father or mother, and the expenses of such proceeding which the Magistrate considers reasonable, and to pay the same to such person as the Magistrate may from time to time direct.</p>

    <p>Permanent Maintenance: </p>

    <p>Permanent maintenance is usually only awarded in cases in which there is a large disparity in the parties’ incomes or earning capacities. Permanent maintenance cases are rare and the length of the marriage is not the determining factor. The goal of any maintenance award is to enable the recipient to become financially self-sufficient with time. Permanent maintenance is typically available only in long-term marriages.  Maintenance is an integral part of all matrimonial proceedings. NB Law Firm tries to get the client the best possible remedy in these cases and get adequate funds for the spouse in need.</p>
